# 출력 : 검 승 : 1 / 흰 승 : 2 / 승부 결정X : 0
# 승부가 결정 났다면 5개의 바둑알중 가장 왼쪽의 바둑알의 가로줄, 세로줄 번호 출력

import sys

# 입력
graph=[]
for _ in range(19):
    graph.append(list(map(int,sys.stdin.readline().split())))


dx=[1,1,0,-1]   # 반시계방향
dy=[0,1,1,1]
# 같은 방향으로만 탐색하는 함수(매번 주위 탐색해서 값이 있다면 다시 양수로 바꾸기)
def search(graph,x,y,color,dir):
    # count
    count = 1
    # 이전 방향에 같은 값이 있을 때
    nx = x - dx[dir]
    ny = y - dy[dir]
    if nx>=0 and nx<19 and ny>=0 and ny<19 and graph[nx][ny] == color:
        return 0
    # 이전 방향에 같은 값 없을 때
    while True:
        nx = x + dx[dir]
        ny = y + dy[dir]
        if nx<0 or nx>=19 or ny<0 or ny>=19:
            return count
        if graph[nx][ny] == color:
            count += 1
            x = nx
            y = ny
            continue
        return count

# main 코드
count = -1
for j in range(19):
    for i in range(19):
        if graph[i][j] == 1 or graph[i][j] == 2:
            # 처음 방향 찾기(가능한 모든 방향으로 이동)
            for k in range(4):
                ni=i+dx[k]
                nj=j+dy[k]
                if ni<0 or ni>=19 or nj<0 or nj>=19:
                    continue
                if graph[ni][nj] == abs(graph[i][j]):
                    count = search(graph,i,j,graph[i][j],k)   # graph,x,y,color,dir
                if count == 5:
                    print(abs(graph[i][j]))
                    print(i+1,j+1)
                    break
            if count == 5: break
    if count == 5: break

if count != 5:
    print(0)

