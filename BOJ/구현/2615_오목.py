# 입력 : 바둑알 (검:1, 흰:2, 빈칸:0) - 숫자 한칸씩 띄워서 표시됨
# 출력 : 검 승 : 1 / 흰 승 : 2 / 승부 결정X : 0
# 승부가 결정 났다면 5개의 바둑알중 가장 왼쪽의 바둑알의 가로줄, 세로줄 번호 출력

import sys

# 입력
graph=[]
for _ in range(19):
    graph.append(list(map(int,sys.stdin.readline().split())))
# 시작 위치
a=-1
b=-1

dx=[1,1,0,-1]   # 반시계방향
dy=[0,1,1,1]
def search(graph,x,y,color,dir,count):
    global a,b
    # 현재 위치 방문처리
    graph[x][y] = (-1) * color
    count += 1
    # print("graph[",x,"][",y,"] : ",count)
    # 주변 탐색
    i=dir   # 현재 방향부터
    for _ in range(4):
        if i>3:
            i -= 4
        nx=x+dx[i]
        ny=y+dy[i]
        if nx<0 or nx>=19 or ny<0 or ny>=19:
            i += 1
            continue
        # 다음 위치가 같은 색
        if graph[nx][ny] == color or graph[nx][ny] == color*(-1):
            # 같은 방향일 때
            if i == dir:
                count = search(graph,nx,ny,color,dir,count)
            # 다른 방향일 때
            else:
                if count == 5:
                    return count
                count = 1
                a=x
                b=y
                count = search(graph,nx,ny,color,i,count)
            # 반환한 count가 5일 때
            if count == 5:
                return count
        # 다음 위치
        i += 1
    return count

# main 코드
count=-1
for j in range(19):
    for i in range(19):
        if graph[i][j] == 1 or graph[i][j] == 2:
            a=i
            b=j
            count = search(graph,i,j,graph[i][j],0,0)
            # print("graph[",a,"][",b,"] => ",count)
            if count == 5:
                print(graph[a][b]*-1)
                print(a+1, b+1)
                break
    if count == 5:
        break

if count != 5:
    print(0)

for i in range(19):
    print(graph[i])


